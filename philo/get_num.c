long	get_num(char *s)
{
	int		sign;
	long	num;

	num = 0;
	while (*s && *s == ' ')
		s++;
	sign = -1 * (*s && *s == '-') + (*s && *s != '-');
	((*s && *s == '-') || (*s && *s == '+')) && s++;
	while (*s && *s >= '0' && *s <= '9')
		num = num * 10 + *(s++) - '0';
	return (sign * num);
}
